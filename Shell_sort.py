def shell_sort(arr):
    n = len(arr)
    gap = n // 2

    while gap > 0:
        for i in range(gap, n):
            temp = arr[i]
            j = i

            while j >= gap and arr[j - gap] > temp:
                arr[j] = arr[j - gap]
                j -= gap

            arr[j] = temp

        gap //= 2

arr = [9,8,3,7,5,6,4,1]
print("Original list:", arr)

shell_sort(arr)

print("Sorted list:", arr)
